<h3>Misión</h3>
<p>JCR Ingenieros es una empresa de Venezuela especializada en venta de plantas eléctricas, servicio de instalación, mantenimiento y operación y soluciones de energía eléctrica que garantizan la productividad de industrias y comercios, contribuyendo a la calidad de vida de la sociedad.
<br><br>
Además, somos distribuidores de motores diesel Cummins en Caracas, Aragua, Los Llanos y Anzoátegui; así como venta de plantas eléctricas Cummins para Curacao, incluyendo la instalación, operación y mantenimiento.
<br><br>
</p>

<h3>Visión y Valores</h3>

<p>Nuestra Visión en JCR Ingenieros es ser líderes en soluciones integrales de energía eléctrica en Venezuela y el Caribe. Contamos para ello con la pasión y conocimiento de profesionales íntegros en su actuación, sumados a la garantía de equipos y repuestos de la más alta calidad, para asegurar ser aliados de nuestros clientes en la continuidad de sus operaciones y el bienestar de la sociedad.
<br><br>
Nos guían 5 valores corporativos:
<br>- <b>Integridad</b>, cumpliendo la s leyes, normas y reglamentos de todos los lugares en los que operamos.
<br>- <b>Disciplina</b>, para comprender los objetivos, orientarnos hacia ellos y autorregularnos.
<br>- <b>Responsabilidad</b>, para hacer lo que se tiene que hacer cuando se tiene que hacer, cumpliendo con los compromisos.
<br>- <b>Compromiso</b>, pensando y actuando con foco en nuestros clientes y la sociedad.
<br>- <b>Pasión</b>, para emocionarnos con cada reto que se nos presenta.
</p>