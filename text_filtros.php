<h3>Filtros y Consumibles</h3>

<p>Como proveedores autorizados Cummins, en JCR Ingenieros representamos la marca de filtros Fleeguard, líder mundial que cuenta con la tecnología más avanzada para garantizar el rendimiento del motor.
<br><br>
Los diseños de los filtros aseguran la protección definitiva de su planta eléctrica, así como su máxima durabilidad.
<br><br>
El portafolio Fleeguard abarca una amplia variedad de filtros de aire, filtros de aceite y filtros de combustible para motogeneradores.
<br><br>
Nuestro personal técnico también está capacitado para orientar a los clientes en la procura de filtros diferenciados para plantas eléctricas Caterpillar o John Deere; así como otras empresas fabricantes, local o internacionalmente, en el momento de realizar los trabajos de mantenimiento.
<br><br>
En los mantenimientos preventivos de los motores de plantas eléctricas es requisito realizar periódicamente el cambio de aceite o lubricantes de motor y el uso de refrigerantes para el sistema de enfriamiento.
<br><br>
En JCR Ingenieros asimismo hacemos la procura de estos consumibles para el mejor funcionamiento del equipo de generación eléctrica que se encuentre en servicio de mantenimiento.</p>
