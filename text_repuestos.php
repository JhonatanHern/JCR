<h3>Repuestos</h3>

<p>La vida útil de una planta eléctrica depende en gran medida del mantenimiento preventivo y el mantenimiento correctivo que, periódicamente, se le haga. Ello requiere de profesionales técnicamente capacitados y del uso de repuestos de calidad.
<br><br>
En JCR Ingenieros hemos creado una robusta plataforma logística y una consolidada red comercial con la que podemos hacer la procura de repuestos originales nuevos para nuestros clientes, que abarca un amplio portafolio con el que ofrecemos la calidad y garantía de servicio de las casas fabricantes:
<br>Cummins, 
<br>Perkins, 
<br>Caterpillar, 
<br>John Deere, 
<br>Detroit Diesel, 
<br>Volvo Penta
<br><br>
Las gestiones de procura de repuestos usados originales para motogeneradores es posible para las marcas fabricantes de las que JCR Ingenieros es proveedor; así como de otras casas, previa evaluación de la obsolescencia del equipo.
<br><br>
Asimismo, en caso de ser necesario durante los trabajos de mantenimiento de plantas eléctricas, el equipo de profesionales de JCR Ingenieros está capacitado para gestionar la compra de repuestos reconstruidos para motores con garantía.</p>
